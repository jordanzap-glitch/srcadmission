                        <h1 class="display-6 mb-4">History of SRC</h1>
                        <p class="mb-2">It all began in the school year 1945-1946.  
                            The slumber of the quiet and sleepy town of Santa Rita was ruffled by a bright idea of woman, Miss Narcisa Gosioco, 
                            a High School teacher in Guagua.  It was she who suggested the need of a secondary school in this town.  
                            This she imparted to the late Doña Irene Pineda, a religious philanthropist of the town.</p>
                        <p class="mb-2">A caucus was called where some civic minded men and women of Sta. Rita took part; 
                        men who were courageous enough to begin from scratch.  
                        Some of these were: Rev. Fr. Restituto Canda, Mr. Eleuterio Calilung, Dr. Felix Coronel. Dr. Francisco Flores, 
                        Miss Narcisa Gosioco, Mr. Conrado Guanzon, Mr. George Mariano, Judge Ramon Miranda, Miss Rosario Pineda, Mr. Pio Saplala, 
                        Judge Alejandro Tayag, Mr. Diego Valencia and others.</p>
                         <p class="mb-2">With much sacrifices amidst threats and discouragements these brave and pioneering 
                            citizens pooled their energies and resources in order to give the youth of Santa Rita an ideal Catholic education.  
                            They put their heads together, studied the pros and cons of the high school, the advantage it would give to the youth,
                            the service it would render the community.  When gray matters and meet ponder over strict matters, something worthwhile 
                            is sure to come out.  Mr. Diego Valencia then was name educational consultant and was sent post haste to Manila to get 
                            the necessary papers.  It was so easy then, it was just like stretching your arm and fishing for the permit. 
                             When everything was ready; books, building, faculty the Superintendent came and gave his blessing and Godspeed.  
                            In April 1947, their labor bore the first fruits, the first graduates of Santa Rita Institute nine (9).</p>
                        <p class="mb-2">1955. Ten years after, a new building was constructed at a total cost of Php 44,000. 
                                 Enrolment was then 450 with 86 graduates in that year.</p>
                        <p class="mb-2">1965. Twenty years after.  The Board of Trustees took a new venture with the opening of the College Department. 
                             A new building amounting to Php46,000 was constructed.  The courses offered were Liberal Arts, Commerce, Education.  
                             Normal and One Year Secretarial Course.  In the same year, the Elementary Department was also opened.  
                            In 1966, the SRI, gained the full college status and officially became the Santa Rita College.</p>
                        <p class="mb-2">Today, the Santa Rita College is now more than seven decades. 
                             Almost every household in Sta. Rita has a graduate in this school.  From the portal have emerged more than eleven thousand graduates. 
                              There are doctors, engineers, chemists, architects, pilots, army and navy men, teachers, businessmen, and even nuns and priests among them.  
                            They all remember that once upon a time there was a beautiful home called Santa Rita Institute..</p>
                        <p class="mb-2">Across the years the institution had stood steadfast with its ideal mission-vision.  
                                The enrolment swelled and from its portals cam great men of letters and luminaries.</p>
                        <p class="mb-2">At the turn of the 21st century, Santa Rita College withstood the perils of Mt. Pinatubo eruption 
                            and the greatest of all plunging economy that decimated the people’s 
                            capacity to seek for quality education.</p>
                        <p class="mb-2">At present, the institution has maintained a prudent fiscal policy to reach out even the impoverished 
                            but deserving students clienteles. In its thrust towards quality education responsive to national development goals, 
                            the institution for the current years has applied and re-opened 
                            collegiate education offering BSED, BEED, BSAcct, BTTE in consortium with DHVTSU and recently the BSIS.</p>

                        

                        
                        
